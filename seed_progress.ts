const rows = csvData.split('\\n').slice(1);

const insert = db.prepare('INSERT INTO progress (user_id, metric_name, metric_value, unit, recorded_at) VALUES (?, ?, ?, ?, ?)');

db.transaction(() => {
  // Clear existing progress for both users to start fresh
  db.prepare('DELETE FROM progress WHERE user_id IN (1, 2)').run();

  for (const userId of [1, 2]) {
    let baseDate = new Date();
    baseDate.setDate(baseDate.getDate() - rows.length * 7); // Start way back

    for (const row of rows) {
      const cols = row.split(',');
      const squat1 = parseFloat(cols[0]);
      const squat2 = parseFloat(cols[1]);
      const squat3 = parseFloat(cols[2]);
      const bench1 = parseFloat(cols[5]);
      const bench2 = parseFloat(cols[6]);
      const bench3 = parseFloat(cols[7]);

      let date = new Date(baseDate);

      if (!isNaN(squat1) && squat1 > 0) {
        insert.run(userId, 'Squat', squat1, 'lbs', date.toISOString());
        date.setDate(date.getDate() + 2);
      }
      if (!isNaN(squat2) && squat2 > 0) {
        insert.run(userId, 'Squat', squat2, 'lbs', date.toISOString());
        date.setDate(date.getDate() + 2);
      }
      if (!isNaN(squat3) && squat3 > 0) {
        insert.run(userId, 'Squat', squat3, 'lbs', date.toISOString());
      }

      date = new Date(baseDate);

      if (!isNaN(bench1) && bench1 > 0) {
        insert.run(userId, 'Bench Press', bench1, 'lbs', date.toISOString());
        date.setDate(date.getDate() + 2);
      }
      if (!isNaN(bench2) && bench2 > 0) {
        insert.run(userId, 'Bench Press', bench2, 'lbs', date.toISOString());
        date.setDate(date.getDate() + 2);
      }
      if (!isNaN(bench3) && bench3 > 0) {
        insert.run(userId, 'Bench Press', bench3, 'lbs', date.toISOString());
      }

      baseDate.setDate(baseDate.getDate() + 7);
    }
  }
})();

